
a = 15
b = 4

# 1. Addition (+)
# Adds two numbers.
print(f"Addition: {a} + {b} = {a + b}")  # Flow: a is added to b

print("-" * 20) # Separator

# 2. Subtraction (-)
# Subtracts the second number from the first.
print(f"Subtraction: {a} - {b} = {a - b}") # Flow: b is subtracted from a

print("-" * 20) # Separator

# 3. Multiplication (*)
# Multiplies two numbers.
print(f"Multiplication: {a} * {b} = {a * b}") # Flow: a is multiplied by b

print("-" * 20) # Separator

# 4. Division (/)
# Divides two numbers. The result is always a float.
print(f"Division: {a} / {b} = {a / b}") # Flow: a is divided by b, resulting in a float

print("-" * 20) # Separator

# 5. Floor Division (//)
# Divides and returns the integer part of the result.
print(f"Floor Division: {a} // {b} = {a // b}") # Flow: a is divided by b, and the decimal part is discarded

print("-" * 20) # Separator

# 6. Modulus (%)
# Returns the remainder of the division.
print(f"Modulus: {a} % {b} = {a % b}") # Flow: a is divided by b, and the remainder is returned

print("-" * 20) # Separator

# 7. Exponentiation (**)
# Raises the first number to the power of the second.
print(f"Exponentiation: {a} ** 2 = {a ** 2}") # Flow: a is raised to the power of 2
print(f"Exponentiation: {b} ** 3 = {b ** 3}") # Flow: b is raised to the power of 3


